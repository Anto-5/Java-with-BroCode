public class Sixth {
    //ARITHMETIC OPERATORS
    public static void main(String[]args){
        int x = 10;
        int y = 2;
        int z;
        z = x + y;
        System.out.println(z);
        z = x - y;
        System.out.println(z);
        z = x * y;
        System.out.println(z);
        z = x / y;
        System.out.println(z);
        z = x % y;
        System.out.println(z);
        x += y;
        System.out.println(x);
        x -= y;
        System.out.println(x);
        x *= y;
        System.out.println(x);
        x /= y;
        System.out.println(x);
        x %= y;
        System.out.println(x);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
    }
    
}
